import datetime


def parse_anchors():
    """将字符串日期转为timestamp，返回排序后的(uid, timestamp)列表"""
    result = []
    for uid, date_str in ANCHORS:
        dt = datetime.datetime.strptime(date_str, "%Y-%m-%d")
        ts = dt.timestamp()
        result.append((uid, ts))
    return sorted(result, key=lambda x: x[0])
